import aiogram

router = aiogram.Router()
@router.message(aiogram.filters.Command("help"))
async def help_command_handler(message: aiogram.types.Message):
    help_text = """👋 Привет! Я бот-трекер задач. Помогу тебе организовать дела.

Вот что я умею:

⌨️ Основное меню (кнопки внизу):
- 📅 Задачи на сегодня — показать список задач на сегодня.
- 🗂️ Списки задач — доступ к просроченным и предстоящим задачам.
- ➕ Добавить задачу — запустить помощника по созданию новой задачи.
- 📊 Статистика — показать твой прогресс, включая "стрик" продуктивности.

⚙️ Доступные команды:
- /start — запуск бота и отображение главного меню.
- /add — добавить новую задачу.
- /today — задачи на сегодня.
- /upcoming — предстоящие задачи.
- /overdue — просроченные задачи.
- /stats — твоя статистика.
- /help — это сообщение.

💡 Как пользоваться:
- Чтобы отметить задачу выполненной, просто нажми на неё в списке.
- Чтобы удалить, нажми на ❌ рядом с задачей."""
    await message.answer(help_text)
